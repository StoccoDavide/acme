using namespace std;

typedef double real_mex;

static void
DATA_NEW(
    mxArray *&mx_id,
    acme::none *ptr)
{
  mx_id = convertPtr2Mat<acme::none>(ptr);
}

static inline acme::none *
DATA_GET(
    mxArray const *&mx_id)
{
  return convertMat2Ptr<acme::none>(mx_id);
}

static void
DATA_DELETE(
    mxArray const *&mx_id)
{
  destroyObject<acme::none>(mx_id);
}

